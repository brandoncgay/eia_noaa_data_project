WITH plant_data AS (
  SELECT
    UTILITY_ID,
    UTILITY_NAME,
    PLANT_CODE,
    PLANT_NAME,
    STREET_ADDRESS,
    CITY,
    STATE,
    ZIP,
    COUNTY,
    LATITUDE,
    LONGITUDE,
    BALANCING_AUTHORITY_CODE,
    BALANCING_AUTHORITY_NAME
  FROM {{ ref('EIA_Plant_Y2023') }} AS EIA_Plant_Y2023
  WHERE
    NOT BALANCING_AUTHORITY_CODE IS NULL
)
SELECT
  *
FROM plant_data
WHERE LATITUDE is not null and LONGITUDE is not null